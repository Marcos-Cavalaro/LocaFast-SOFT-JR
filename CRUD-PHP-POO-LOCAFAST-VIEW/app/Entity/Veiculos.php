<?php

namespace App\Entity;

use \App\DB\Database;
use \PDO;

class veiculos
{

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var integer
     */

    public $id;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var string
     */

    public $placa;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var string
     */

    public $ano_fabricacao;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var boolean
     */

    public $status;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var integer
     */

    public $id_categoria;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var integer
     */

    public $id_modelo;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function cadastrarVeiculos()
    {

        // INSERIR A VAGA NO BANCO
        $obDatabase = new Database('veiculos');
        

        // ATRIBUIR O ID DA VAGA NA INSTANCIA
        $this->id = $obDatabase->insert([

            'placa' => $this->placa,
            'ano_fabricacao' => $this->ano_fabricacao,
            'status' => $this->status,
            'id_categoria' => $this->id_categoria,
            'id_modelo' => $this->id_modelo,
        ]);

        //RETORNAR SUCESSO
        return true;
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function atualizarVeiculos()
    {
        return (new Database('veiculos'))->update('id = ' . $this->id, [

            'placa' => $this->placa,
            'ano_fabricacao' => $this->ano_fabricacao,
            'status' => $this->status,
            'id_categoria' => $this->id_categoria,
            'id_modelo' => $this->id_modelo,

        ]);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function excluirVeiculos()
    {
        return (new Database('veiculos'))->delete('id = ' . $this->id,);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /**
     * @param string $where
     * @param string $order
     * @param string $limit
     * @return array
     */

    public static function getVeiculos($where = null, $order = null, $limit = null)
    {
        return (new Database('veiculos'))->select($where, $order, $limit)
            ->fetchAll(PDO::FETCH_CLASS, SELF::class);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /**
     * @param integer $id
     * @return veiculos
     */

    public static function getVeiculo($id)
    {
        return (new Database('veiculos'))->select('id =' . $id)
            ->fetchObject(self::class);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

}