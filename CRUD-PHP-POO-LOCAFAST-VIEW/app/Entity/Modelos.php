<?php

namespace App\Entity;

use \App\DB\Database;
use \PDO;

class modelos
{

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var integer
     */

    public $id;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var string
     */

    public $nome;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var boolean
     */

    public $status;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var integer
     */

    public $id_marca;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function cadastrarModelos()
    {

        // INSERIR A VAGA NO BANCO
        $obDatabase = new Database('modelos');
        

        // ATRIBUIR O ID DA VAGA NA INSTANCIA
        $this->id = $obDatabase->insert([

            'nome' => $this->nome,
            'status' => $this->status,
            'id_marca' => $this->id_marca
        ]);

        //RETORNAR SUCESSO
        return true;
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function atualizarModelos()
    {
        return (new Database('modelos'))->update('id = ' . $this->id, [

            'nome' => $this->nome,
            'status' => $this->status

        ]);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function excluirModelos()
    {
        return (new Database('modelos'))->delete('id = ' . $this->id,);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /**
     * @param string $where
     * @param string $order
     * @param string $limit
     * @return array
     */

    public static function getModelos($where = null, $order = null, $limit = null)
    {
        return (new Database('modelos'))->select($where, $order, $limit)
            ->fetchAll(PDO::FETCH_CLASS, SELF::class);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /**
     * @param integer $id
     * @return modelos
     */

    public static function getModelo($id)
    {
        return (new Database('modelos'))->select('id =' . $id)
            ->fetchObject(self::class);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

}