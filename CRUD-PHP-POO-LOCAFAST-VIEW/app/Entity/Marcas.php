<?php

namespace App\Entity;

use \App\DB\Database;
use \PDO;

class marcas
{

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var integer
     */

    public $id;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var string
     */

    public $nome;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var boolean
     */

    public $status;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function cadastrarMarcas()
    {

        // INSERIR A VAGA NO BANCO
        $obDatabase = new Database('marcas');
        

        // ATRIBUIR O ID DA VAGA NA INSTANCIA
        $this->id = $obDatabase->insert([

            'nome' => $this->nome,
            'status' => $this->status

        ]);

        //RETORNAR SUCESSO
        return true;
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function atualizarMarcas()
    {
        return (new Database('marcas'))->update('id = ' . $this->id, [

            'nome' => $this->nome,
            'status' => $this->status

        ]);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function excluirMarcas()
    {
        return (new Database('marcas'))->delete('id= ' . $this->id,);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /**
     * @param string $where
     * @param string $order
     * @param string $limit
     * @return array
     */

    public static function getMarcas($where = null, $order = null, $limit = null)
    {
        return (new Database('marcas'))->select($where, $order, $limit)
            ->fetchAll(PDO::FETCH_CLASS, SELF::class);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /**
     * @param integer $id
     * @return marcas
     */

    public static function getMarca($id)
    {
        return (new Database('marcas'))->select('id= ' . $id)
            ->fetchObject(self::class);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

}