    public $nome_completo;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @var string
     */

    public $cpf;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\


    /** 
     * 
     * @var string
     */

    public $data_nascimento;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\


    /** 
     * 
     * @var string
     */

    public $endereco;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\


    /** 
     * 
     * @var string
     */

    public $telefone;

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
