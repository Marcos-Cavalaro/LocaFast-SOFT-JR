    /** 
     * 
     * @return boolean 
     */

    public function cadastrarUsuarios()
    {

        // INSERIR A VAGA NO BANCO
        $obDatabase = new Database('usuarios');


        // ATRIBUIR O ID DA VAGA NA INSTANCIA
        $this->id = $obDatabase->insert([

            'nome_completo' => $this->nome_completo,
            'cpf' => $this->cpf,
            'data_nascimento' => $this->data_nascimento,
            'endereco' => $this->endereco,
            'telefone' => $this->telefone,
            'status' => $this->status,
            'senha' => $this->senha,
            'tipo_usuario' => $this->tipo_usuario,
            'email' => $this->email,
        ]);

        $status = "success";

        //RETORNAR SUCESSO
        return true;
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function atualizarUsuarios()
    {
        print("<script>console.log('AQUI')</script>");

        return (new Database('usuarios'))->update('id = ' . $this->id, [

            'nome_completo' => $this->nome_completo,
            'cpf' => $this->cpf,
            'data_nascimento' => $this->data_nascimento,
            'endereco' => $this->endereco,
            'telefone' => $this->telefone,
            'status' => $this->status,
            'senha' => $this->senha,
            'tipo_usuario' => $this->tipo_usuario,
            'email' => $this->email,

        ]);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /** 
     * 
     * @return boolean 
     */

    public function excluirUsuarios()
    {
        return (new Database('usuarios'))->delete('id = ' . $this->id,);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /**
     * @param string $where
     * @param string $order
     * @param string $limit
     * @return array
     */

    public static function getUsuarios($where = null, $order = null, $limit = null)
    {
        return (new Database('usuarios'))->select($where, $order, $limit)
            ->fetchAll(PDO::FETCH_CLASS, SELF::class);
    }

//----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

    /**
     * @param integer $id
     * @return usuarios
     */

    public static function getUsuario($id)
    {
        return (new Database('usuarios'))->select('id =' . $id)
            ->fetchObject(self::class);
    }

    //----------------------------------------------------------------------------------------------------------------------------------------------------------------------------\\

}
